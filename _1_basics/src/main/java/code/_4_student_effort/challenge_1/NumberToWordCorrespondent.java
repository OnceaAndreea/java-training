package code._4_student_effort.challenge_1;

public class NumberToWordCorrespondent {

    private int number;
    private String word;

    public NumberToWordCorrespondent(int number, String word) {
        this.number = number;
        this.word = word;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }
}
